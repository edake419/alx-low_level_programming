/**
 *_strlen -length of a string
 *@s: A pointer to an int that will be changed/updated
 *
 *Return: void that means our answer is correct
 */

int _strlen(char *s)
{
        int length, index;

	length = 0;

	for (index = 0; *(s + index) != '\0'; index++)
	{
		length++;
	}

	return (length);
}
